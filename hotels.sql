-- Tables
SELECT * FROM RESORTS;
SELECT * FROM HOTELS;
SELECT * FROM ROOMS;
SELECT * FROM GUESTS;
SELECT * FROM BOOKINGS;

-- R1
SELECT
    NS, NOMS, CAPCH
FROM
    RESORTS
WHERE
    TYPES = 'montagne';

-- R2
SELECT
    NOMH, NH, ADRH, TELH, CATH
FROM
    HOTELS H, RESORTS R
WHERE
    H.NS = R.NS
    AND TYPES = 'mer';

-- R3
SELECT
    DISTINCT NOMS
FROM
    RESORTS R, HOTELS H
WHERE
    R.NS = H.NS
    AND TYPES = 'mer'
    AND CATH = 4;

-- R4
SELECT
    DISTINCT NOMCL, ADRCL
FROM
    GUESTS G, BOOKINGS B, RESORTS R
WHERE
    G.NCL = B.NCL
    AND B.NS = R.NS
    AND TYPES = 'montagne';

-- R5
SELECT
    NCH, H.NH
FROM
    ROOMS RM, HOTELS H, RESORTS R
WHERE
    RM.NH = H.NH
    AND RM.NS = R.NS
    AND H.NS = R.NS
    AND CATH = 2
    AND TYPES = 'montagne'
    AND PRIX < 50;
